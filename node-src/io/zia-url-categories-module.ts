/** Match engine.gen_env._module_source for a single-resource env root. */
function moduleSource(moduleDir: string, envDir: string): string {
  let moduleSrc = path.relative(
    envDir,
    path.join(moduleDir, ZIA_URL_CATEGORIES_RESOURCE_TYPE),
  );
  if (path.sep !== "/") {
    moduleSrc = moduleSrc.split(path.sep).join("/");
  }
  if (
    !moduleSrc.startsWith("../")
    && !moduleSrc.startsWith("./")
    && !moduleSrc.startsWith("/")
  ) {
    moduleSrc = `./${moduleSrc}`;
  }
  return moduleSrc;
}

/**
 * Exact post-fmt env root main.tf matching engine.gen_env.render_env_main
 * for label/member zia_url_categories with local state (no backend).
 */
function renderEnvMain(tenant: string, moduleSrc: string): string {
  // Python %r for a safe opaque tenant yields single-quoted form.
  const tenantRepr = `'${tenant}'`;
  return (
    `# GENERATED by engine.gen_env for tenant ${tenantRepr} — do not edit.\n`
    + `# Regenerate: make gen-env TENANT=${tenant}\n`
    + "\n"
    + "terraform {\n"
    + '  required_version = ">= 1.5"\n'
    + "  required_providers {\n"
    + `    ${ZIA_PROVIDER_NAME} = {\n`
    + `      source = "${ZIA_PROVIDER_SOURCE}"\n`
    + "    }\n"
    + "  }\n"
    + "  # local state — opt into remote state with\n"
    + `  # make gen-env TENANT=${tenant} BACKEND=azurerm\n`
    + "}\n"
    + "\n"
    + `provider "${ZIA_PROVIDER_NAME}" {\n`
    + "  # credentials via provider environment variables\n"
    + "}\n"
    + "\n"
    + 'variable "items" {\n'
    + "  # opaque at the root; the module enforces the strict type.\n"
    + "  type = any\n"
    + "}\n"
    + "\n"
    + `module "${ZIA_URL_CATEGORIES_RESOURCE_TYPE}" {\n`
    + `  source = "${moduleSrc}"\n`
    + "  items  = var.items\n"
    + "}\n"
  );
}

function modulePaths(workspace: string, tenant: string): ZiaUrlCategoriesModulePaths {
  const moduleDir = path.join(
    workspace,
    "modules",
    MODULE_SET,
    ZIA_URL_CATEGORIES_RESOURCE_TYPE,
  );
  const envDir = path.join(workspace, "envs", tenant, ZIA_URL_CATEGORIES_RESOURCE_TYPE);
  return Object.freeze({
    envMain: path.join(envDir, "main.tf"),
    moduleMain: path.join(moduleDir, "main.tf"),
    moduleOutputs: path.join(moduleDir, "outputs.tf"),
    moduleVariables: path.join(moduleDir, "variables.tf"),
    moduleVersions: path.join(moduleDir, "versions.tf"),
  });
}

/** Write the five Terraform runtime files for zia_url_categories into workspace. */
export async function writeZiaUrlCategoriesModule(options: {
  readonly tenant: string;
  readonly workspace: string;
}): Promise<ZiaUrlCategoriesModulePaths> {
  if (
    typeof options.workspace !== "string"
    || !path.isAbsolute(options.workspace)
    || options.workspace.includes("\0")
  ) {
    return fail(
      "INVALID_ZIA_MODULE_WORKSPACE",
      "ZIA module workspace must be an absolute directory",
    );
  }
  if (!TENANT.test(options.tenant)) {
    return fail(
      "INVALID_ZIA_MODULE_TENANT",
      "tenant must match [A-Za-z0-9_.-]+ and not be . or ..",
    );
  }
  const layout = await prepareZiaUrlCategoriesWorkspace(options);
  const workspace = layout.root;
  await assertZiaUrlCategoriesWorkspace(layout);
  const paths = modulePaths(workspace, options.tenant);
  const moduleSetDir = path.join(workspace, "modules", MODULE_SET);
  const envDir = path.dirname(paths.envMain);
  const moduleSrc = moduleSource(moduleSetDir, envDir);
  const contents = Object.freeze({
    envMain: renderEnvMain(options.tenant, moduleSrc),
    moduleMain: renderModuleMain(),
    moduleOutputs: renderModuleOutputs(),
    moduleVariables: renderModuleVariables(),
    moduleVersions: renderModuleVersions(),
  });
  await Promise.all([
    writeZiaUrlCategoryPrivateFile(paths.moduleMain, contents.moduleMain),
    writeZiaUrlCategoryPrivateFile(paths.moduleVariables, contents.moduleVariables),
    writeZiaUrlCategoryPrivateFile(paths.moduleOutputs, contents.moduleOutputs),
    writeZiaUrlCategoryPrivateFile(paths.moduleVersions, contents.moduleVersions),
    writeZiaUrlCategoryPrivateFile(paths.envMain, contents.envMain),
  ]);
  await assertZiaUrlCategoriesWorkspace(layout, Object.values(paths));
  return paths;
}
