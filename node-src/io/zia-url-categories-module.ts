/** Opaque tenant label — same gate as PR-1 artifact paths. */
const TENANT = /^(?!\.{1,2}$)[A-Za-z0-9_.-]+$/;

const MODULE_SET = `zia-v${ZIA_PROVIDER_VERSION}`;

export interface ZiaUrlCategoriesModulePaths {
  readonly envMain: string;
  readonly moduleMain: string;
  readonly moduleOutputs: string;
  readonly moduleVariables: string;
  readonly moduleVersions: string;
}

function fail(code: string, message: string, category: "domain" | "io" = "domain"): never {
  throw new ProcessFailure({ code, category, message });
}

