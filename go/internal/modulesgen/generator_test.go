// TestRendererPreservesCompleteSevenFileContractAndCoreShapes verifies the
// module file contract against current packs.
func TestRendererPreservesCompleteSevenFileContractAndCoreShapes(t *testing.T) {
	root := committedRoot(t)
	segment, err := RenderModuleFiles(root, "zpa_segment_group")
	if err != nil {
		t.Fatalf("RenderModuleFiles(zpa_segment_group): %v", err)
	}
	if names := segment.Names(); !reflect.DeepEqual(names, ExpectedModuleFiles) {
		t.Fatalf("Names() = %v, want %v", names, ExpectedModuleFiles)
	}

	mainTf, _ := segment.Get(FileMain)
	mustMatch(t, mainTf, `^# GENERATED by iw modules generate from packs/zpa/schemas/provider/zpa\.json — do not edit\.\n# Regenerate: make gen-modules\n`)
	mustNotMatch(t, mainTf, `python|engine\.gen_module`)
	readme, _ := segment.Get(FileReadme)
	mustMatch(t, readme, "Regenerate with `iw modules generate` or `make gen-modules`\\.")
	mustNotMatch(t, readme, `python|engine\.gen_module`)
	mustMatch(t, mainTf, `for_each = var\.items`)
	mustMatch(t, mainTf, `(?s)dynamic "applications".*each\.value\.applications == null \? \[\] : each\.value\.applications`)
	mustNotMatch(t, mainTf, `\n  id =`)

	variablesTf, _ := segment.Get(FileVariables)
	mustMatch(t, variablesTf, `(?s)type = map\(object\(\{.*name = string.*description = optional\(string\)`)

	outputsTf, _ := segment.Get(FileOutputs)
	mustMatch(t, outputsTf, `output "name_to_id"`)

	sample, _ := segment.Get(FileSampleTfvars)
	wantSample := "{\n  \"items\": {\n    \"example\": {\n      \"name\": \"example\"\n    }\n  }\n}\n"
	if sample != wantSample {
		t.Errorf("sample.auto.tfvars.json = %q, want %q", sample, wantSample)
	}

	categories, err := RenderModuleFiles(root, "zia_url_categories")
	if err != nil {
		t.Fatalf("RenderModuleFiles(zia_url_categories): %v", err)
	}
	categoriesVariables, _ := categories.Get(FileVariables)
	mustMatch(t, categoriesVariables, `scopes = optional\(set\(object\(\{`)
	mustMatch(t, categoriesVariables, `url_keyword_counts = optional\(object\(\{`)
	mustMatch(t, categoriesVariables, `scope_entities = optional\(object\(\{`)
	mustNotMatch(t, categoriesVariables, `category_id`)
	categoriesOutputs, _ := categories.Get(FileOutputs)
	mustNotMatch(t, categoriesOutputs, `name_to_id`)
}

