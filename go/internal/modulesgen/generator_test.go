// packSetFile is the minimal shape this test reads from a committed
// packs/<name>.packset.json document: just enough to physically reduce a pack
// root to the packs (and shared components) a profile actually needs.
type packSetFile struct {
	Packs  []string `json:"packs"`
	Shared []string `json:"shared"`
}

func readPackSetFile(t *testing.T, path string) packSetFile {
	t.Helper()
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read %s: %v", path, err)
	}
	var doc packSetFile
	if err := json.Unmarshal(data, &doc); err != nil {
		t.Fatalf("unmarshal %s: %v", path, err)
	}
	return doc
}

// copyDir recursively copies src to dst.
func copyDir(t *testing.T, src, dst string) {
	t.Helper()
	err := filepath.WalkDir(src, func(path string, entry os.DirEntry, err error) error {
		if err != nil {
			return err
		}
		relative, relErr := filepath.Rel(src, path)
		if relErr != nil {
			return relErr
		}
		target := filepath.Join(dst, relative)
		if entry.IsDir() {
			return os.MkdirAll(target, 0o755)
		}
		data, readErr := os.ReadFile(path)
		if readErr != nil {
			return readErr
		}
		if mkErr := os.MkdirAll(filepath.Dir(target), 0o755); mkErr != nil {
			return mkErr
		}
		return os.WriteFile(target, data, 0o644)
	})
	if err != nil {
		t.Fatalf("copyDir %s -> %s: %v", src, dst, err)
	}
}

// TestEveryCommittedProfileDrivesGenerationFromItsPhysicallyReducedPackRoot
// ports "every committed profile drives generation from its physically
// reduced pack root".
func TestEveryCommittedProfileDrivesGenerationFromItsPhysicallyReducedPackRoot(t *testing.T) {
	root := repoRoot(t)
	expected := map[string]int{
		"full": 151, "zscaler": 151, "zcc": 7, "zia": 74, "zpa": 54, "ztc": 16,
		"empty": 0, "aws": 0, "cloudflare": 0, "google": 0, "netbox": 0,
	}
	profileNames := make([]string, 0, len(expected))
	for name := range expected {
		profileNames = append(profileNames, name)
	}
	sort.Strings(profileNames)

	for _, profileName := range profileNames {
		count := expected[profileName]
		t.Run(profileName, func(t *testing.T) {
			directory := t.TempDir()
			output := t.TempDir()
			profilePath := filepath.Join(root, "packs", profileName+".packset.json")
			doc := readPackSetFile(t, profilePath)
			for _, name := range doc.Packs {
				copyDir(t, filepath.Join(root, "packs", name), filepath.Join(directory, name))
			}
			for _, name := range doc.Shared {
				copyDir(t, filepath.Join(root, "packs", "_shared", name), filepath.Join(directory, "_shared", name))
			}
			loaded, err := metadata.LoadPackRoot(metadata.LoadPackRootOptions{
				PacksRoot:   directory,
				ProfilePath: &profilePath,
			})
			if err != nil {
				t.Fatalf("LoadPackRoot: %v", err)
			}
			generated, err := GenerateActiveModules(loaded, GenerateModuleOptions{
				OutputRoot: output,
				FormatHCL:  IdentityFormatter,
			})
			if err != nil {
				t.Fatalf("GenerateActiveModules: %v", err)
			}
			if len(generated) != count {
				t.Fatalf("generated length = %d, want %d", len(generated), count)
			}
			total := 0
			for _, module := range generated {
				total += len(module.Files)
			}
			if total != count*7 {
				t.Errorf("total generated files = %d, want %d", total, count*7)
			}
			if _, err := ValidateGeneratedModuleTree(output, ActiveGeneratedResourceTypes(loaded)); err != nil {
				t.Errorf("ValidateGeneratedModuleTree: %v", err)
			}
		})
	}
}

// TestHCLFormatterMatchesTerraformAcrossFullGeneratedCorpus proves that the
// in-process token formatter is byte-identical to terraform fmt for every HCL
// artifact produced by the full 151-resource profile. Terraform formats one
// temporary tree recursively so this differential gate does not recreate the
// per-file subprocess cost that production has removed.
func TestHCLFormatterMatchesTerraformAcrossFullGeneratedCorpus(t *testing.T) {
	if testing.Short() {
		t.Skip("full generated-corpus Terraform differential skipped under -short")
	}
	executable := terraformExecutable(t)
	root := committedRoot(t)
	formatter := NewHCLFormatter()
	oracleRoot := t.TempDir()

	type formattedFile struct {
		path string
		want string
	}
	var formatted []formattedFile
	for _, resourceType := range ActiveGeneratedResourceTypes(root) {
		rendered, err := RenderModuleFiles(root, resourceType)
		if err != nil {
			t.Fatalf("RenderModuleFiles(%s): %v", resourceType, err)
		}
		for _, name := range ExpectedModuleFiles {
			if !needsTerraformFormat(name) {
				continue
			}
			source, ok := rendered.Get(name)
			if !ok {
				t.Fatalf("RenderModuleFiles(%s) omitted %s", resourceType, name)
			}
			want, err := formatter.FormatHCL(source)
			if err != nil {
				t.Fatalf("FormatHCL(%s/%s): %v", resourceType, name, err)
			}
			path := filepath.Join(oracleRoot, resourceType, string(name))
			if err := os.MkdirAll(filepath.Dir(path), 0o700); err != nil {
				t.Fatalf("MkdirAll(%s): %v", filepath.Dir(path), err)
			}
			if err := os.WriteFile(path, []byte(source), 0o600); err != nil {
				t.Fatalf("WriteFile(%s): %v", path, err)
			}
			formatted = append(formatted, formattedFile{path: path, want: want})
		}
	}

	command := exec.Command(executable, "fmt", "-recursive", oracleRoot)
	if output, err := command.CombinedOutput(); err != nil {
		t.Fatalf("terraform fmt -recursive %s: %v\n%s", oracleRoot, err, output)
	}
	for _, file := range formatted {
		got, err := os.ReadFile(file.path)
		if err != nil {
			t.Fatalf("ReadFile(%s): %v", file.path, err)
		}
		if string(got) != file.want {
			relative, relErr := filepath.Rel(oracleRoot, file.path)
			if relErr != nil {
				relative = file.path
			}
			t.Errorf("%s differs after hclwrite.Format and terraform fmt", relative)
		}
	}
	if len(formatted) != 151*5 {
		t.Errorf("compared = %d, want %d", len(formatted), 151*5)
	}
}

