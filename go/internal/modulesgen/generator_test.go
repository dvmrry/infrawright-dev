// TestResourceOwnedMainOverrideAndJSONSampleComeOnlyThroughTheLoader ports
// "resource-owned main override and JSON sample come only through the
// loader".
func TestResourceOwnedMainOverrideAndJSONSampleComeOnlyThroughTheLoader(t *testing.T) {
	_, root := syntheticRoot(t, syntheticRootOptions{
		MainOverride: "# resource-owned override\n",
		Sample:       metadata.JsonObject{"name": "chosen", "extra": float64(7)},
	})
	output := t.TempDir()

	mainOverride, err := root.LoadResourceMainOverride("sample_resource")
	if err != nil {
		t.Fatalf("LoadResourceMainOverride: %v", err)
	}
	if mainOverride == nil || *mainOverride != "# resource-owned override\n" {
		t.Fatalf("LoadResourceMainOverride = %v, want %q", mainOverride, "# resource-owned override\n")
	}

	rendered, err := RenderModuleFiles(root, "sample_resource")
	if err != nil {
		t.Fatalf("RenderModuleFiles: %v", err)
	}
	main, _ := rendered.Get(FileMain)
	if main != "# resource-owned override\n" {
		t.Errorf("main.tf = %q, want %q", main, "# resource-owned override\n")
	}

	sample, _ := rendered.Get(FileSampleTfvars)
	var decoded struct {
		Items struct {
			Example map[string]any `json:"example"`
		} `json:"items"`
	}
	if err := json.Unmarshal([]byte(sample), &decoded); err != nil {
		t.Fatalf("unmarshal sample: %v", err)
	}
	want := map[string]any{
		"extra": float64(7),
		"name":  "chosen",
		"rules": []any{map[string]any{"enabled": true}},
	}
	if !reflect.DeepEqual(decoded.Items.Example, want) {
		t.Errorf("sample.items.example = %#v, want %#v", decoded.Items.Example, want)
	}

	generated, err := GenerateModule(root, "sample_resource", GenerateModuleOptions{
		OutputRoot: output,
		FormatHCL:  IdentityFormatter,
	})
	if err != nil {
		t.Fatalf("GenerateModule: %v", err)
	}
	if len(generated.Files) != 7 {
		t.Errorf("generated.Files length = %d, want 7", len(generated.Files))
	}

	validated, err := ValidateGeneratedModuleTree(output, []string{"sample_resource"})
	if err != nil {
		t.Fatalf("ValidateGeneratedModuleTree: %v", err)
	}
	if !reflect.DeepEqual(validated, []string{"sample_resource"}) {
		t.Errorf("ValidateGeneratedModuleTree = %v, want [sample_resource]", validated)
	}
}

